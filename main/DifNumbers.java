public class DifNumbers {

   public static void main(String[] args) {
      int num1 = 5, num2 = 15; 
	  int dif;
      dif = num1 - num2;
      System.out.println("Dif of these numbers: " + dif);
   }
   
   public static void main(String[] args) {
      int num1 = 3, num2 = 18; 
	  int del;
      del = num1 / num2;
      System.out.println("Del of these numbers: " + del);
   }
}